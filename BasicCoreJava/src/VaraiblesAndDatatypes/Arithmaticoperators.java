public class Arithmaticoperators {

	public static void main(String[] args) {
		
		// +,-,*,/,%
		
		int a=10;
		int b=20;
		
		// + operators
		System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(a*b);
		System.out.println(b/a);
		
		//%
		System.out.println(b%a);

	}

}
